    digest = hmac.new(
        C3_ARGUSVERIFY_SECRET.encode("utf-8"),
        canonical_json_text(signed).encode("utf-8"),
        hashlib.sha256,
    ).hexdigest()
    return f"hmac-sha256:{digest}"


